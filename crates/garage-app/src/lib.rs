//! Прикладной слой автосервиса.
//!
//! Этот crate находится между чистым доменом и внешним миром. Его задача -
//! описать бизнес-сценарии и порты, через которые сценарии получают и
//! сохраняют агрегаты. Здесь по-прежнему нет Telegram, SQLx, PostgreSQL,
//! dotenv и любых деталей транспорта или хранения.
//!
//! Граница ответственности такая:
//! - `garage-domain` хранит инварианты отдельных сущностей и value objects;
//! - `garage-app` координирует несколько агрегатов в рамках одного сценария;
//! - `garage-infra` позже реализует порты репозиториев через PostgreSQL;
//! - `garage-telegram` позже будет только переводить команды пользователя в
//!   вызовы application services.
//!
//! Важный принцип: application service не должен дублировать доменные правила.
//! Например, он не проверяет вручную, можно ли закрыть ремонт, а вызывает
//! `Repair::complete`. Зато он обязан проверить связи между агрегатами:
//! автомобиль принадлежит клиенту, booking относится к тому же автомобилю,
//! поставка пополняет существующую складскую позицию.

