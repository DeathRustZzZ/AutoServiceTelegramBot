//! Номер телефона как доменный value object.
//!
//! Главная задача этого модуля - не просто хранить строку, а один раз привести
//! пользовательский ввод к предсказуемому каноническому виду. После успешного
//! `PhoneNumber::parse` остальная доменная модель может считать, что телефон:
//! - не пустой;
//! - относится к поддерживаемому белорусскому формату;
//! - хранится в едином виде `+375XXXXXXXXX`.
//!
//! Такой подход убирает повторные проверки из бизнес-логики. Сервисы,
//! обработчики Telegram-команд и инфраструктурный слой работают уже не с
//! произвольной строкой, а с валидным доменным значением.

use thiserror::Error;

/// Ошибка нормализации телефонного номера.
///
/// Ошибки разделены намеренно: пустой ввод обычно требует другого сообщения
/// пользователю, чем ввод с неправильным форматом. При этом enum остается
/// небольшим и не раскрывает детали алгоритма наружу.
#[derive(Debug, Clone, PartialEq, Eq, Error)]
pub enum PhoneNumberError {
    /// Пользователь ничего не ввел или передал строку только из пробелов.
    #[error("phone number is empty")]
    Empty,

    /// После очистки строка не соответствует поддерживаемым белорусским форматам.
    #[error("invalid Belarus phone number format")]
    InvalidFormat,
}

/// Печатает номер в том же каноническом виде, в котором он хранится внутри.
///
/// `Display` нужен для UI, логов и сообщений пользователю. Важно, что здесь нет
/// повторной нормализации: если `PhoneNumber` уже создан, его инварианты были
/// проверены на входе в `parse`.
impl std::fmt::Display for PhoneNumber {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.0)
    }
}

/// Валидный телефонный номер в каноническом формате `+375XXXXXXXXX`.
///
/// Внутренняя строка закрыта от прямого изменения. Это защищает инвариант типа:
/// нельзя создать `PhoneNumber("abc")` или сохранить номер в случайном формате,
/// минуя алгоритм `parse`.
#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub struct PhoneNumber(String);

impl PhoneNumber {
    /// Разбирает пользовательский ввод и приводит номер к единому формату.
    ///
    /// Алгоритм нормализации:
    ///
    /// 1. Убираем пробелы по краям строки. Это позволяет спокойно принимать ввод
    ///    из Telegram и форм, где пользователь мог случайно добавить пробел.
    /// 2. Отдельно проверяем пустую строку. Так вызывающий код может отличить
    ///    отсутствие номера от номера, который похож на данные, но не прошел
    ///    форматную проверку.
    /// 3. Оставляем только ASCII-цифры. Благодаря этому поддерживаются привычные
    ///    человеку варианты записи: `+375 (29) 123-45-67`,
    ///    `375291234567`, `8 029 123 45 67`.
    /// 4. Проверяем поддерживаемый префикс:
    ///    - `375` и 12 цифр уже соответствует международному белорусскому виду;
    ///    - `80` и 11 цифр считается локальной записью, которую переводим в
    ///      международный вид заменой `80` на `375`.
    /// 5. Добавляем `+` и сохраняем только канонический результат.
    ///
    /// Метод сознательно не проверяет, существует ли номер у оператора и
    /// принадлежит ли он конкретному клиенту. Это уже не синтаксис номера, а
    /// бизнес- или интеграционная проверка.
    pub fn parse(input: &str) -> Result<Self, PhoneNumberError> {
        // Сначала нормализуем только внешний шум. Внутренние разделители пока не
        // трогаем, потому что они будут удалены единым проходом при сборе цифр.
        let trimmed = input.trim();

        if trimmed.is_empty() {
            return Err(PhoneNumberError::Empty);
        }

        // Один проход по строке собирает только цифры. Это проще и устойчивее,
        // чем перечислять все допустимые разделители вроде пробелов, скобок и
        // дефисов: новый визуальный разделитель не сломает валидный номер.
        let digits: String = trimmed.chars().filter(|ch| ch.is_ascii_digit()).collect();

        // На этом шаге строка уже состоит только из цифр, поэтому проверка
        // сводится к длине и префиксу. Результат всегда приводится к цифрам без
        // плюса, а плюс добавляется один раз в самом конце.
        let normalized_digits = if digits.len() == 12 && digits.starts_with("375") {
            digits
        } else if digits.len() == 11 && digits.starts_with("80") {
            // Локальный белорусский формат `80XXXXXXXXX` переводим в
            // международный `375XXXXXXXXX`, отбрасывая первые две цифры `80`.
            format!("375{}", &digits[2..])
        } else {
            return Err(PhoneNumberError::InvalidFormat);
        };

        // Единственная точка создания значения. После нее `PhoneNumber` всегда
        // хранится одинаково, что упрощает сравнение, хеширование и вывод.
        Ok(Self(format!("+{}", normalized_digits)))
    }

    /// Возвращает номер как строковый срез без передачи владения.
    ///
    /// Это безопасный способ отдать значение наружу в базу данных, API или
    /// шаблон сообщения, не позволяя вызывающему коду изменить внутреннюю строку
    /// и нарушить инвариант `PhoneNumber`.
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

#[cfg(test)]
mod tests {
    use super::{PhoneNumber, PhoneNumberError};

    /// Проверяем основной happy path: международный белорусский номер уже
    /// содержит код страны, а парсер только приводит внешний вид к канону.
    #[test]
    fn parse_accepts_international_number_with_visual_separators() {
        let phone = PhoneNumber::parse("+375 (29) 123-45-67").unwrap();

        assert_eq!(phone.as_str(), "+375291234567");
    }

    /// Пользователь может прислать номер без плюса. Для домена это тот же самый
    /// международный формат, поэтому результат должен быть идентичным.
    #[test]
    fn parse_accepts_international_number_without_plus() {
        let phone = PhoneNumber::parse("375291234567").unwrap();

        assert_eq!(phone.as_str(), "+375291234567");
    }

    /// Локальная запись через `80` поддерживается ради пользовательского ввода,
    /// но внутри системы все равно хранится международный `+375`.
    #[test]
    fn parse_normalizes_local_belarus_number_to_international_format() {
        let phone = PhoneNumber::parse("8 029 123 45 67").unwrap();

        assert_eq!(phone.as_str(), "+375291234567");
    }

    /// Пробелы по краям не должны ломать валидный номер: это типичный шум из
    /// Telegram-сообщений, copy-paste и HTML-форм.
    #[test]
    fn parse_ignores_outer_whitespace() {
        let phone = PhoneNumber::parse("  +375291234567  ").unwrap();

        assert_eq!(phone.as_str(), "+375291234567");
    }

    /// Пустой ввод выделен в отдельную ошибку, чтобы прикладной слой мог
    /// показать более точное сообщение пользователю.
    #[test]
    fn parse_rejects_empty_input() {
        let error = PhoneNumber::parse("   ").unwrap_err();

        assert_eq!(error, PhoneNumberError::Empty);
    }

    /// Если после очистки цифр номер не похож ни на `375...`, ни на `80...`,
    /// это форматная ошибка, а не отсутствие значения.
    #[test]
    fn parse_rejects_unsupported_country_code() {
        let error = PhoneNumber::parse("+7 (999) 123-45-67").unwrap_err();

        assert_eq!(error, PhoneNumberError::InvalidFormat);
    }

    /// Длина важна так же, как и префикс: номер с кодом страны, но без нужного
    /// количества цифр нельзя считать валидным доменным значением.
    #[test]
    fn parse_rejects_number_with_invalid_length() {
        let error = PhoneNumber::parse("+37529123456").unwrap_err();

        assert_eq!(error, PhoneNumberError::InvalidFormat);
    }

    /// `Display` не форматирует номер заново, а показывает уже сохраненный
    /// канонический вид. Это защищает UI и логи от разных представлений номера.
    #[test]
    fn display_returns_canonical_phone_number() {
        let phone = PhoneNumber::parse("8 (029) 123-45-67").unwrap();

        assert_eq!(phone.to_string(), "+375291234567");
    }

    /// `as_str` должен отдавать ссылку на каноническое значение без копирования.
    /// Для вызывающего кода это основной путь сохранить номер в БД или отправить
    /// его во внешний API.
    #[test]
    fn as_str_returns_canonical_phone_number() {
        let phone = PhoneNumber::parse("+375-29-123-45-67").unwrap();

        assert_eq!(phone.as_str(), "+375291234567");
    }
}
